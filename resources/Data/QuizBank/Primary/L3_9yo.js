export const getL2QuestionsBySubject = (subject) => {
  return L3_questions[subject] || [];
};

export const getRandomL2Questions = (count = 5) => {
  const allQuestions = getAllL2Questions();
  const shuffled = [...allQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};